use gpui::{App, Pixels, StyleRefinement, Window, prelude::*, px, svg};

use crate::{ActiveTheme, StyledExt};

#[derive(IntoElement)]
pub struct Icon {
    variant: IconVariant,
    size: IconSize,

    style: StyleRefinement,
}

impl Icon {
    pub fn new(variant: IconVariant, size: IconSize) -> Self {
        Self { variant, size, style: StyleRefinement::default() }
    }
}

impl RenderOnce for Icon {
    fn render(self, _window: &mut Window, cx: &mut App) -> impl IntoElement {
        svg()
            .path(format!("icon/{}.svg", self.variant.name()))
            .size(Pixels::from(self.size))
            .text_color(cx.theme().fg_primary)
            .refine_style(&self.style)
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum IconSize {
    ExtraSmall,
    Small,
    Regular,
    Large,
}

impl From<IconSize> for Pixels {
    fn from(size: IconSize) -> Self {
        match size {
            IconSize::ExtraSmall => px(16.0),
            IconSize::Small => px(20.0),
            IconSize::Regular => px(24.0),
            IconSize::Large => px(32.0),
        }
    }
}

